my_string = "Hello, World!"
slice_result = my_string[7:12]
print(slice_result)
# Вывод: World

my_list = [1, 2, 3, 4, 5]
slice_result = my_list[1:4]
print(slice_result)
# Вывод: [2, 3, 4]

my_tuple = (10, 20, 30, 40, 50)
slice_result = my_tuple[2:5]
print(slice_result)
# Вывод: (30, 40, 50)

my_bytes = b"Hello, World!"
slice_result = my_bytes[7:12]
print(slice_result)
# Вывод: b'World'
